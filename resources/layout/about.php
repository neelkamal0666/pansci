						Brief Research Highlights:<br />

<b>Developed 12 case studies on Technology Transformation:<b>
<p>The Organizations covered are Bharath Earth Movers Ltd., Lakshmi Machine Works Ltd., Alfa Laval Ltd., Crompton Greaves Ltd., Elgi Equipments Ltd., Escorts Ltd., KCP Ltd., Kelvinator India Ltd., L&T McNeil Ltd., Mahindra & Mahindra Ltd., Stovec Screens Ltd., and Tractors & Farms Equipments Ltd. These case studies were developed as a part of the project funded by Department of Science and Technology, Government of India.</p>
<b>Career and Reward Management of Professionals:</b>
<p>Studies were conducted to understand and relate compensation, performance recognition, and pay satisfaction of R & D personnel. Restructuring of the scientific personnel cadre in the central Government establishment was the outcome of a consultancy study. A study was also completed to understand the nature of uncertainty experienced R & D personnel. These experiences were related to Job Involvement, Work Involvement and Work Motivation. Another study examined the degree of team working and issues of team management. A national survey amongst personnel professionals was conducted to understand functionally dependent stress. Over 8000 questionnaires were mailed and obtained 550 responses. The study identified the nature of stressors and coping styles. The National Institute of Personnel Management funded this study.</P>
<b>Innovation and Innovativeness:</b>
<p>Conceptually innovation and innovativeness were differentiated and measures were developed to understand organizational innovativeness. Based upon the conceptual framework and users interaction two measures were developed, grounded on subsystems, to measure organizational innovativeness and innovation. Empirical testing was done on a sample of 43 firms, guided by a preliminary (10 firms) and a pilot studies (27 firms). Exploratory factor analysis identified organizational innovativeness as a bi-factorial construct constituting factors â€“ adaptability and problem solving skills. Path analytic model was developed to examine the causal linkage between environment, R & D intensity, innovativeness and innovations.</p>
<b>Comparative Human Resource Management:</b>
<p>Studies were initiated to examine cross-cultural business perceptions of managers. The data was collected over 500 managers from India and similar studies have been completed in Germany, USA and China. This study helped in understanding stereotypes and preparing managers for international careers. A collaborative study also has been initiated to compare the careers of scientists in India, Japan, Indonesia, Singapore, Korea and Germany. The data was collected from Indian Scientists and over 500 scientists and engineers responded from the Telecom, IT and Pharmaceutical sectors.</p>
<b>Organizational aspects of Technology Management:</b>
<p>Technology transformation process in the two manufacturing sectors in India was successfully studied. A survey methodology was used and the data from 73 organizations in the automotive and machine building sectors were collected for the purpose of statistical analysis. The descriptive analysis has shown that there is no significant difference between the sectors, indicating that there is considerable homogeneity in the technology adoption, adaptation and innovation objectives and activities across the sectors. Another study focused on the functional role of R & D and linked the role to overall technological performance. The data was obtained from 65 organizations. Process variable consisted of nature of work, linkage, organizational support, and past performance. Structure variable included organizational importance, control, research infrastructure and planning. Environment of R & D consisted of mobility and research environment variable. Structure emerged as a more powerful discriminating factor. Another study focused on the techno organizational changes due to CNC Technology.</p>
<b>Developing an Integrated Technology Management Program:</b>
<p>Scientists and engineers in the future need to be trained in several areas other than the core of Science and Engineering. They require an understanding of the organizational and managerial processes on the one hand and legal and environmental aspects on the other. I intend to focus on the HRD aspects of working scientists and engineers. A detailed study will be made on the current on the job development and off the job development of scientists and engineers. An assessment will also be made on the available mechanisms, institutions and programs on technology management. Based on these an integrated technology management program will be developed.</p>
<b>Evolving Performing Organizations Through People:</b>
<p>Organizations have to evolve various strategies in the future to sustain and increase their performance. The basic issues are around how to provide focus and direction and achieve the required alignment at all levels, how to develop required competencies in terms of techniques, attitudes, knowledge and skills, and how to integrate reward and punishment system. These will demand development of a personnel assessment methodology, personnel grading system, an ability development system, methods of organizational diagnosis and development etc., Studies will be initiated in these areas including action research studies.</p>

						


						
